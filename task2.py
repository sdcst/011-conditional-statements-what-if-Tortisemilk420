"""
 Have the user input a number 
 Determine if the number is positive, negative or 0 
 2 points

 Inputs:
 number

 Outputs:
 - "positive"
 - "negative"
 - "zero"

 Example:

Enter a number: 3
positive

Enter a number: -1.2
negative
"""
num = -1.2

if 0 < num:

   print(f"positive")

elif 0 > num:

  print("negative")

elif 0 == num:

  print("zero")

